async function main() {
  const cars = await prisma.car.findMany();
  for (const car of cars) {
    let estValue = MSRP_MAP[car.name];
    if (!estValue) {
      estValue = Math.round((car.pricePerDay * 250) / 1000) * 1000;
      console.log(`Missing MSRP for ${car.name}, defaulting to ${estValue}`);
    } else {
      console.log(`Updating ${car.name} -> $${estValue.toLocaleString()}`);
    }
    await prisma.car.update({
      where: { id: car.id },
      data: { estimatedValue: estValue }
    });
  }
}

main().catch(console.error).finally(() => prisma.$disconnect());
